#include "libft.h"

void	ft_putzero_fd(char *s, int fd)
{
	int i;

	i = 0;
	while (s[i] != 0)
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\0", 1);
	i++;
	while (s[i] != 0)
	{
		write(fd, &s[i], 1);
		i++;
	}
}
